int64 C(int n, int r) {
	if(r < 0 || r > n)
		return 0;
	int64 &ans = Com[n][r];
	if(ans > -1)
		return ans;
	if(r==0 || r==n)
		ans = 1;
	else
		ans = C(n-1, r-1) + C(n-1, r);
	return ans;
}


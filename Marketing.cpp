void dfs(int i, int curHt) {
  if(fail)
    return;
  if(ht[i] == curHt)
    return;
  if(ht[i] == -1) {
    ht[i] = curHt;
    for(auto it = (G[i]).begin(); it != (G[i]).end(); ++it) {
      dfs(*it, 1-curHt);
    }
  }
  else
    fail = true;
}


vector <int> topHt, botHt, topRd, botRd;
int N, M;
vector <int> lt(55), rt(55);
vector < bool > vis(55);

bool isEdge(int l, int r) {
	return topRd[l] < botRd[r] && topRd[l] * botHt[r] < topHt[l]*botRd[r];
}

bool dfs(int l) {
	if(l == -1)
		return true;
	if(vis[l])
		return false;
	vis[l] = true;

	for(int r = 0; r < M; ++r) {
		if(isEdge(l, r) && dfs(rt[r])) {
			rt[r] = l;
			lt[l] = r;
			return true;
		}
	}
	return false;
}


bool wakesUp(int S) {
    for(int t = 1; t <= (int)100000; ++t) {
        S += D;
        for(int i = 0; i < (int)N; ++i) {
            if((t-st[i])%per[i] == 0) {
                S -= vol[i];
            }
        }
        if(S <= 0)
            return true;
    }

    return false;
}

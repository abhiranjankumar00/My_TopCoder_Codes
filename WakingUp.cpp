int binarySearch(int l, int r) {
    if(l == r)
        return l;
    int mid = (l+r)/2;

    int waked = wakesUp(mid);
    //printf("l = %d, r = %d, mid = %d, wakesUp = %d\n", l, r, mid, waked);
    if(waked)
        return binarySearch(mid+1, r);
    else
        return binarySearch(l, mid);
}

